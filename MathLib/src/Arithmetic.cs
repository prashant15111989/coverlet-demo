using System;

namespace MathLib
{
    public class Arithmetic
    {
        public static double Add(double a,double b)
        {
            return a + b;
        }
        public static double Multiply(double a, double b)
        {
            return a * b;
        }
        public static double Sub(double a, double b)
        {
            return a - b;
        }
        public static double Div(double a, double b)
        {
            return a / b;
        }
    }
}
